// function declaration
function add(a, b) {
  return a + b;
}

console.log(add(4, 15)); // 19
console.log(add(18, 5)); // 23
console.log(add(33, 7)); // 40
console.log(add(8, 9)); // 17

// function expression
const add2 = function (a, b) {
  return a + b;
};

console.log(add2(10, 5)); // 15
console.log(add2(30, 5)); // 35
